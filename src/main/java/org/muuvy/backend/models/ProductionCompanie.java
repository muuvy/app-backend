package org.muuvy.backend.models;

public class ProductionCompanie {

    private Integer Integer;
    private String logoPath;
    private String name;
    private Integer originCountry;

    public java.lang.Integer getInteger() {
        return Integer;
    }

    public void setInteger(java.lang.Integer integer) {
        Integer = integer;
    }

    public String getLogoPath() {
        return logoPath;
    }

    public void setLogoPath(String logoPath) {
        this.logoPath = logoPath;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public java.lang.Integer getOriginCountry() {
        return originCountry;
    }

    public void setOriginCountry(java.lang.Integer originCountry) {
        this.originCountry = originCountry;
    }
}
